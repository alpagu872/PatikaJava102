package Z_New_era.I_Misc.TypeCasting;

public class Main {
    public static void main(String[] args) {
        int a = 10;
        double b = a;
        System.out.println(b);



        double pi = 3.14;
        int aa = (int) pi;
        System.out.println(aa);

        String str = String.valueOf(aa);
        System.out.println(str);
    }
}
